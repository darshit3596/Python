name = "darshit"
print(name.capitalize())  # Capitalizes the first letter
print(name.upper())       # Converts all letters to uppercase
print(name.lower())       # Converts all letters to lowercase
print(name.title())       # Capitalizes the first letter of each word
print(name.swapcase())    # Swaps the case of each letter  DaRsHiT      small
print(name.replace("d", "k"))  # Replaces 'd' with 'D           