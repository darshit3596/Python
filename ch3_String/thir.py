print(name[0:3])

print(name[3:])  # Prints from index 3 to the end = shit

print(name[1:5])  # Prints from index 1 to index 4  =arsh

print(name[:3])  # Prints every second character = dar 

print(name[3:])  # Prints every second character = shit

print(name[::2])  # Prints every second character = drs

